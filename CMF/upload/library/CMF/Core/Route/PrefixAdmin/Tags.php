<?php

/**
 * Route prefix handler for phrases in the admin control panel.
 *
 * @package XenForo_Phrases
 */
class CMF_Core_Route_PrefixAdmin_Tags implements XenForo_Route_Interface
{
	/**
	 * Match a specific route for an already matched prefix.
	 *
	 * @see XenForo_Route_Interface::match()
	 */
	public function match($routePath, Zend_Controller_Request_Http $request, XenForo_Router $router)
	{
		$action = $router->resolveActionWithIntegerParam($routePath, $request, 'tag_id');
		return $router->getRouteMatch('CMF_Core_ControllerAdmin_Tag', $action, 'tags');
	}

	/**
	 * Method to build a link to the specified page/action with the provided
	 * data and params.
	 *
	 * @see XenForo_Route_BuilderInterface
	 */
	public function buildLink($originalPrefix, $outputPrefix, $action, $extension, $data, array &$extraParams)
	{
		return XenForo_Link::buildBasicLinkWithIntegerParam($outputPrefix, $action, $extension, $data, 'tag_id', 'tag');
	}
}